#ifndef FIRSTCHAR_H
#define FIRSTCHAR_H

namespace wiselib {
	
	/**
	 * @brief Trivial hash function that returns the value of the first byte
     * in the string (or 0, if the length of the string is 0).
     * Useful as baseline for performance comparisons.
	 * 
	 * @ingroup Hash_concept
	 */
	template<
		typename OsModel_P
	>
	class Firstchar {
		
		public:
			typedef OsModel_P OsModel;
			typedef typename OsModel::block_data_t block_data_t;
			typedef typename OsModel::size_t size_type;
			typedef ::uint8_t hash_t;
			
			enum { MAX_VALUE = (hash_t)(-1) };
			
			static hash_t hash(const block_data_t *s, size_type l) {
				return (l > 0) ? s[0] : 0;
			}
		
		private:
		
	}; // Firstchar
}

#endif // FIRSTCHAR_H

